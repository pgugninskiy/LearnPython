"""
Модуль исключений для приложения телефонного справочника.

Содержит пользовательские исключения, связанные с валидацией и обработкой контактов.
"""


class ContactError(Exception):
    """
    Базовый класс для всех исключений, связанных с контактами.

    Наследуется от Exception. Используется для централизованного перехвата
    ошибок, специфичных для работы с контактами.
    """
    pass


class EmptyFieldError(ContactError):
    """
    Исключение, возникающее при попытке создать или обновить контакт
    с пустыми обязательными полями (например, имя или телефон).

    :param message: Пользовательское сообщение об ошибке.
                    По умолчанию: "Имя и телефон обязательны для заполнения."
    """

    def __init__(self, message: str = "Имя и телефон обязательны для заполнения.") -> None:
        super().__init__(message)


class InvalidPhoneError(ContactError):
    """
    Исключение, возникающее при передаче некорректного номера телефона.

    Требование: номер должен состоять только из цифр и иметь длину не менее 10 символов.

    :param message: Пользовательское сообщение об ошибке.
                    По умолчанию: "Телефон должен содержать минимум 10 цифр."
    """

    def __init__(self, message: str = "Телефон должен содержать минимум 10 цифр.") -> None:
        super().__init__(message)


class InvalidEmailError(ContactError):
    """
    Исключение, возникающее при передаче некорректного email-адреса.

    Требование: строка должна содержать '@' и '.'.

    :param message: Пользовательское сообщение об ошибке.
                    По умолчанию: "Неверный формат email (пример: user@example.com)."
    """

    def __init__(self, message: str = "Неверный формат email (пример: user@example.com).") -> None:
        super().__init__(message)